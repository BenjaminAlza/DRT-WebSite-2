use Illuminate\Notifications\Notifiable;

class User extends Authenticatable
{
    use Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'usu_nombres','usu_apepaterno','usu_apematerno','usu_email' ,'usu_login','password','usu_estado'
    ];
}
